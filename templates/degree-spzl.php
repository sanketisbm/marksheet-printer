function titleCase($string)
{
    $string = (string) $string;
    if ($string === '') {
        return '';
    }

    return mb_convert_case($string, MB_CASE_TITLE, "UTF-8");
}

function english_to_hindi_unicode($text)
{
    $text = trim((string) $text);
    if ($text === '') {
        return $text;
    }

    // You can normalize common patterns if you want:
    // $text = str_replace(["D/o", "S/o", "W/o"], ["Daughter of", "Son of", "Wife of"], $text);

    $url = "https://inputtools.google.com/request?text=" . urlencode($text)
        . "&itc=hi-t-i0-und&num=1";

    $ch = curl_init();
    curl_setopt($ch, CURLOPT_URL, $url);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
    // For local/XAMPP ‚Äì disable SSL verification. On production, you should enable this.
    curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, false);
    curl_setopt($ch, CURLOPT_SSL_VERIFYHOST, false);

    $response = curl_exec($ch);
    curl_close($ch);

    if ($response === false || $response === '') {
        // API not reachable ‚Üí fallback to original
        return $text;
    }

    $json = json_decode($response, true);

    // Correct extraction:
    if (
        isset($json[1][0][1][0])
    ) {
        return $json[1][0][1][0];
    }
    // If API doesn‚Äôt return proper structure
    return $text;
}

